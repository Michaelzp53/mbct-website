      '用这三层过滤，能把选址的决策风险降到最低。',
      '八、避坑指南：这些坑千万别踩',
      '说了这么多方法，最后来点实在的——我这些年见过的选址大坑，给大家提个醒。',
      '坑一：被"低价"冲昏头脑',
      '租金便宜一定有便宜的原因。我见过太多人看到低价就冲进去，最后被各种隐性成本吞掉。',
      '我有一个客户，租下了一个价格只有市场价60%的物业，沾沾自喜觉得自己捡了便宜。结果开业前发现，那个物业是军产，消防审批根本过不了，改造成本比市场价高三倍。前后折腾了一年多，最后还是放弃了，白白浪费了几十万的定金和设计费。',
