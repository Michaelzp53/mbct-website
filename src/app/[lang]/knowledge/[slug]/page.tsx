      '分析商圈，还有一个不可忽视的维度，就是竞争态势。你要搞清楚这个商圈里，有多少酒店、都是什么档次、各自的经营状况如何。',
      '我一般会做一个"竞争地图"，把商圈内所有酒店标注出来，包括位置、档次、房间数、房价区间、OTA评分。然后去实地住一遍、体验一遍，做到心中有数。',
      '这里有个重要的概念要引入——"有效竞争"。不是所有酒店都是你的竞争对手。比如你开经济型酒店，对面的五星级豪华酒店就不是你的直接竞争，因为价格差了太多层。你真正的竞争对手，是跟你定位相近、价格相近、客群相似的那些酒店。',
      '我见过一个案例，一个投资人在某三线城市开了一家主打亲子牌的精品酒店。开业前做调研，发现周边五公里内有十几家酒店，觉得竞争太激烈了，心里打退堂鼓。但我们帮他一分析，那十几家里，真正做亲子定位的只有一家，而且口碑一般。他差异化竞争的空间其实很大。后来他坚持开了那家店，开业第一年平均入住率就达到了72%，比周边同行高了近二十个百分点。',
      '所以商圈分析的核心，是搞清楚你在跟谁竞争、竞争什么、我们的优势在哪里。',
      '三、人流热力图：看得见的数据密码',
      '说完商圈分析，接下来讲一个这几年特别火的工具——人流热力图。这东西说起来简单，就是用不同颜色标注一个区域的人群密集程度，红色最热、蓝色最冷。但怎么解读这张图，怎么把热力图数据转化成选址决策的依据，这里面的门道可不少。',
      '热力图能告诉你什么',
