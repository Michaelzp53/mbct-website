      '比如一个地铁换乘站，人流超级密集，24小时都是红色。但这些人大多是匆匆赶路的通勤者，他们不是酒店的目标客户。你在换乘站旁边开酒店，看到的是海量人流，转化出来的入住却寥寥无几。',
      '再比如一个城市CBD的十字路口，白天热力图红得发紫，全是办公人群。但到了晚上和周末，这里就变成冷清的"鬼城"，因为白领们都回家了。你如果开商务酒店，选在这里没错；但如果开度假民宿，选在这里就是灾难。',
      '所以解读热力图的第一步，是搞清楚这个区域的人流是什么时间段、什么性质的。',
      '黄金时段分析法',
      '我常用的方法是"黄金时段分析法"。我会把一天分成几个时段：早高峰（7-9点）、上午（9-12点）、午间（12-14点）、下午（14-18点）、晚高峰（18-20点）、晚间（20-24点）、深夜（0-6点）。然后分别看各个时段的热力分布。',
      '对于商务酒店来说，最重要的是上午和下午的热力值，因为这代表商务活动的活跃程度。如果一个区域白天红、晚上蓝，说明是纯粹的商务区，适合商务酒店；晚上也红，说明有夜生活配套，更适合做休闲度假或者娱乐型酒店。',
      '我之前帮一个客户在重庆选址，看中了一个区域，热力图显示白天人流量很大，夜间也不差。乍一看挺完美。但仔细分析发现，夜间的人群主要集中在几个火锅店和酒吧周边，不是酒店的有效客源。最后客户听取建议，把位置往东移了两公里，虽然租金贵了一点，但开业后入住率比预期高了15%。',
      '工作日与周末的差异',
      '热力图还有一个重要的分析维度，就是工作日和周末的差异。',
